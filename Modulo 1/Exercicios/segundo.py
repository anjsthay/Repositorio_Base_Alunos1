# + - soma
# - - subtraçao
# / - divisao
# * - multiplicaçao
# % - resto da divisao
# ** - elevado
#int(), float()
numero1 = int(input("digite o primeiro numero"))
numero2 = int(input("digite o segundo numero "))
resultado = numero1 + numero2 , numero1 - numero2, numero1 * numero2, numero1 % numero2, numero1 / numero2, numero1 ** numero2
print(resultado)

print(numero1, "+", numero2, "=", numero1 + numero2)
print(f"{numero1} + {numero2} = {numero1} + {numero2}")

